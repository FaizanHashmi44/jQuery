$(document).ready(function(){
    $('#btn').click(function(){
        $('.card').css('display', 'block');
    });
    $('.cross').click(function(){
        $('.card').css('display', 'none');
    });
    $('.cancel').click(function(){
        $('.card').css('display', 'none');
    });
    $('.btn2').click(function(){
        $('.card').css('display', 'none');
    });
})